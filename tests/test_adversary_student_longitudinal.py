def _make_record(
    student_id: str = "s001",
    week: int = 1,
    question_sn: int = 1,
    concept_coverage: float = 0.7,
    llm_rubric: float = 0.6,
    ensemble_score: float = 0.65,
    rasch_ability: float = 0.5,
) -> LongitudinalRecord:
    return LongitudinalRecord(
        student_id=student_id,
        week=week,
        question_sn=question_sn,
        scores={
            "concept_coverage": concept_coverage,
            "llm_rubric": llm_rubric,
            "ensemble_score": ensemble_score,
            "rasch_ability": rasch_ability,
        },
        tier_level=2,
        tier_label="test",
    )


def _build_store(tmp_path, records: list[LongitudinalRecord]) -> LongitudinalStore:
    path = str(tmp_path / "store.yaml")
    store = LongitudinalStore(path)
    for rec in records:
        store.add_record(rec)
    return store


def _make_student_data(
    weeks=None, student_id="s001", student_name="Test", class_name="A",
) -> StudentLongitudinalData:
    if weeks is None:
        weeks = [1, 2, 3]
    scores_by_week = {}
    for w in weeks:
        scores_by_week[w] = {
            1: {
                "concept_coverage": 0.5 + 0.05 * w,
                "llm_rubric": 0.4 + 0.05 * w,
                "ensemble_score": 0.45 + 0.05 * w,
                "rasch_ability": -0.2 + 0.1 * w,
            },
            2: {
                "concept_coverage": 0.4 + 0.05 * w,
                "llm_rubric": 0.35 + 0.05 * w,
                "ensemble_score": 0.40 + 0.05 * w,
                "rasch_ability": -0.3 + 0.1 * w,
            },
        }
    return StudentLongitudinalData(
        student_id=student_id,
        student_name=student_name,
        class_name=class_name,
        weeks=weeks,
        scores_by_week=scores_by_week,
        trend_slope=0.05 if len(weeks) >= 2 else None,
        trend_direction="상승" if len(weeks) >= 2 else "데이터 부족",
        percentiles_by_week={w: 50.0 for w in weeks},
    )


def _make_cohort(weeks=None) -> CohortDistribution:
    if weeks is None:
        weeks = [1, 2, 3]
    cohort = CohortDistribution()
    for w in weeks:
        scores = [0.3 + 0.05 * i + 0.02 * w for i in range(10)]
        cohort.weekly_scores[w] = scores
        cohort.weekly_q_scores[w] = {
            1: [0.4 + 0.05 * i + 0.02 * w for i in range(10)],
            2: [0.3 + 0.05 * i + 0.02 * w for i in range(10)],
        }
        arr = np.array(scores)
        cohort.weekly_stats[w] = CohortWeekStats(
            week=w,
            median=float(np.median(arr)),
            q1=float(np.percentile(arr, 25)),
            q3=float(np.percentile(arr, 75)),
            min=float(np.min(arr)),
            max=float(np.max(arr)),
            mean=float(np.mean(arr)),
            std=float(np.std(arr, ddof=0)),
            n=len(scores),
        )
    return cohort


